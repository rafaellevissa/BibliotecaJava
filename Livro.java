public class Livro {
    private String editora;
    private String autores;
    private int edicao;
    private int ano;
    
    public String getEditora(){
        return editora;
    }
    public String getAutores(){
        return autores;
    }
    public int getEdicao(){
        return edicao;
    }
    public int getAno(){
        return ano;
    }
}
